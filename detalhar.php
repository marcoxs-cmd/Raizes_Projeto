    <h1>Detalhes da Atividade</h1>
    <p>
      Nesta atividade, você irá explorar os conceitos fundamentais do empreendedorismo social, aprender como identificar oportunidades para resolver problemas sociais e desenvolver soluções inovadoras que possam ter um impacto positivo na sociedade.
    </p>

    <h2>Objetivo da Atividade:</h2>
    <p>
      O objetivo desta atividade é proporcionar uma compreensão profunda sobre o empreendedorismo social e capacitar os alunos a pensarem criativamente sobre como transformar desafios sociais em oportunidades de negócios com impacto positivo.
    </p>

    <h2>O que você aprenderá:</h2>
    <ul>
      <li>Compreensão do conceito de empreendedorismo social e sua relevância no mundo atual.</li>
      <li>Identificação de problemas sociais e como transformar esses problemas em oportunidades de negócios.</li>
      <li>Como criar e gerenciar negócios com foco em impacto social sustentável.</li>
      <li>Estratégias para medir e aumentar o impacto social de iniciativas empresariais.</li>
      <li>Boas práticas para a gestão de empresas sociais e como escalar essas iniciativas.</li>
    </ul>

    <h2>Estrutura da Atividade:</h2>
    <p>
      A atividade será dividida em 4 módulos principais. Cada módulo incluirá leituras, vídeos e atividades práticas para ajudá-lo a aplicar o que aprendeu.
    </p>

    <ul>
      <li><strong>Módulo 1:</strong> Introdução ao Empreendedorismo Social. Aqui você aprenderá sobre os conceitos-chave e a história do empreendedorismo social.</li>
      <li><strong>Módulo 2:</strong> Identificação e Solução de Problemas Sociais. Neste módulo, exploraremos como identificar problemas sociais e criar soluções inovadoras.</li>
      <li><strong>Módulo 3:</strong> Criando um Modelo de Negócio Social. Este módulo abordará como criar um modelo de negócio focado em gerar impacto social sustentável.</li>
      <li><strong>Módulo 4:</strong> Casos Reais de Empreendedorismo Social. Estudo de casos de empresas sociais bem-sucedidas e suas melhores práticas.</li>
    </ul>

    <h2>Atividades Práticas:</h2>
    <p>
      Durante os módulos, você será convidado a desenvolver suas próprias ideias de negócios sociais, aplicar ferramentas para avaliar o impacto e apresentar um modelo de negócio inovador focado em impacto social.
    </p>

    <h2>Avaliação:</h2>
    <p>
      A avaliação será baseada no desenvolvimento do seu projeto de negócio social, incluindo a aplicação de conceitos aprendidos nos módulos. Você precisará entregar um relatório final com a descrição do seu modelo de negócio, incluindo estratégias para medir o impacto social.
    </p>
